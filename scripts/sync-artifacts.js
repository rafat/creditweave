const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const CONTRACTS_DIR = path.join(ROOT, 'contracts');
const DEPLOYMENTS_FILE = path.join(CONTRACTS_DIR, 'deployments.json');

const FRONTEND_DIR = path.join(ROOT, 'frontend');
const CRE_DIR = path.join(ROOT, 'cre', 'my-workflow');

const CONTRACT_NAMES = [
    'UnderwritingRegistry',
    'NAVOracle',
    'RWALendingPool',
    'RWAAssetRegistry',
    'InvestorShareToken',
    'MockERC20'
];

function sync() {
    console.log('--- Syncing Artifacts ---');

    // 1. Read deployments
    if (!fs.existsSync(DEPLOYMENTS_FILE)) {
        console.error('deployments.json not found in contracts/ folder. Run deployment script first.');
        process.exit(1);
    }
    const deployments = JSON.parse(fs.readFileSync(DEPLOYMENTS_FILE, 'utf8'));
    console.log('Loaded deployments:', deployments);

    // 2. Collect ABIs
    const abis = {};
    for (const name of CONTRACT_NAMES) {
        const artifactPath = path.join(CONTRACTS_DIR, 'out', `${name}.sol`, `${name}.json`);
        if (fs.existsSync(artifactPath)) {
            const artifact = JSON.parse(fs.readFileSync(artifactPath, 'utf8'));
            abis[name] = artifact.abi;
            console.log(`Extracted ABI for ${name}`);
        } else {
            console.warn(`Artifact for ${name} not found at ${artifactPath}`);
        }
    }

    // 3. Generate TypeScript output for Frontend
    const frontendAbisDir = path.join(FRONTEND_DIR, 'lib', 'abis');
    if (!fs.existsSync(frontendAbisDir)) fs.mkdirSync(frontendAbisDir, { recursive: true });

    let frontendContent = `// Automatically generated by scripts/sync-artifacts.js\n`;
    frontendContent += `export const DEPLOYMENTS = ${JSON.stringify(deployments, null, 2)} as const;\n\n`;
    for (const [name, abi] of Object.entries(abis)) {
        frontendContent += `export const ${name.toUpperCase()}_ABI = ${JSON.stringify(abi, null, 2)} as const;\n\n`;
    }
    
    fs.writeFileSync(path.join(frontendAbisDir, 'index.ts'), frontendContent);
    console.log(`Updated frontend artifacts at ${path.join(frontendAbisDir, 'index.ts')}`);

    // 4. Generate JSON/TS output for CRE
    const creAbisDir = path.join(CRE_DIR, 'abis');
    if (!fs.existsSync(creAbisDir)) fs.mkdirSync(creAbisDir, { recursive: true });
    
    let creContent = `// Automatically generated by scripts/sync-artifacts.js\n`;
    creContent += `export const DEPLOYMENTS = ${JSON.stringify(deployments, null, 2)} as const;\n\n`;
    for (const [name, abi] of Object.entries(abis)) {
        creContent += `export const ${name.toUpperCase()}_ABI = ${JSON.stringify(abi, null, 2)} as const;\n\n`;
    }
    
    fs.writeFileSync(path.join(creAbisDir, 'index.ts'), creContent);
    console.log(`Updated CRE artifacts at ${path.join(creAbisDir, 'index.ts')}`);

    // 5. Update CRE Configs
    const configEnvs = ['staging', 'production'];
    for (const env of configEnvs) {
        const configPath = path.join(CRE_DIR, `config.${env}.json`);
        if (fs.existsSync(configPath)) {
            const config = JSON.parse(fs.readFileSync(configPath, 'utf8'));
            config.underwritingRegistryAddress = deployments.underwritingRegistry;
            config.navOracleAddress = deployments.navOracle;
            config.lendingPoolAddress = deployments.lendingPool;
            config.rwaAssetRegistryAddress = deployments.rwaAssetRegistry;
            fs.writeFileSync(configPath, JSON.stringify(config, null, 2));
            console.log(`Updated CRE config at ${configPath}`);
        }
    }

    console.log('--- Sync Complete ---');
}

sync();
